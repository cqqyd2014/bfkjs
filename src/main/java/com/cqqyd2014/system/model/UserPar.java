package com.cqqyd2014.system.model;

public class UserPar {
	String com_id;
	String user_id;
	String par_code;
	String par_desc;
	String par_value;
	public String getCom_id() {
		return com_id;
	}
	public void setCom_id(String com_id) {
		this.com_id = com_id;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getPar_code() {
		return par_code;
	}
	public void setPar_code(String par_code) {
		this.par_code = par_code;
	}
	public String getPar_desc() {
		return par_desc;
	}
	public void setPar_desc(String par_desc) {
		this.par_desc = par_desc;
	}
	public String getPar_value() {
		return par_value;
	}
	public void setPar_value(String par_value) {
		this.par_value = par_value;
	}

}
