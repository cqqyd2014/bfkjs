package com.cqqyd2014.wh.model;

public class SnMove {
	String dat;
	String from_wh_id;
	String from_wh_name;
	String to_wh_id;
	String to_wh_name;
	String move_no;
	String move_type;
	public String getDat() {
		return dat;
	}
	public void setDat(String dat) {
		this.dat = dat;
	}
	public String getFrom_wh_id() {
		return from_wh_id;
	}
	public void setFrom_wh_id(String from_wh_id) {
		this.from_wh_id = from_wh_id;
	}
	public String getFrom_wh_name() {
		return from_wh_name;
	}
	public void setFrom_wh_name(String from_wh_name) {
		this.from_wh_name = from_wh_name;
	}
	public String getTo_wh_id() {
		return to_wh_id;
	}
	public void setTo_wh_id(String to_wh_id) {
		this.to_wh_id = to_wh_id;
	}
	public String getTo_wh_name() {
		return to_wh_name;
	}
	public void setTo_wh_name(String to_wh_name) {
		this.to_wh_name = to_wh_name;
	}
	public String getMove_no() {
		return move_no;
	}
	public void setMove_no(String move_no) {
		this.move_no = move_no;
	}
	public String getMove_type() {
		return move_type;
	}
	public void setMove_type(String move_type) {
		this.move_type = move_type;
	}
}
